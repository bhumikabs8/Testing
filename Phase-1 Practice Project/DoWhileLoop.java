package demo;

public class DoWhileLoop 
{
	public static void main(String[] args) 
	{
		int count = 1;

        // Using a do-while loop to print numbers from 1 to 5
        do {
            System.out.println(count);
            count++;
        } while (count <= 5);

        System.out.println("Loop finished!");
    }

}


